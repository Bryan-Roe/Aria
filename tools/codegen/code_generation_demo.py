GENERATED_CSV_PARSER = '''
def parse_csv_line(line: str, delimiter: str) -> list:
    """Parse a CSV line into individual field values, handling quoted strings"""
    fields = []
    current_field = ""
    in_quotes = False
    i = 0

    while i < len(line):
        char = line[i]

        if char == '"':
            in_quotes = not in_quotes
        elif char == delimiter and not in_quotes:
            fields.append(current_field.strip())
            current_field = ""
        else:
            current_field += char

        i += 1

    fields.append(current_field.strip())
    return fields
'''

GENERATED_SLUG_GENERATOR = '''
def slugify(title: str) -> str:
    """Convert a title string to a URL-friendly slug (lowercase, hyphens)"""
    import re
    # Convert to lowercase
    slug = title.lower()
    # Replace spaces with hyphens
    slug = slug.replace(' ', '-')
    # Remove special characters except hyphens
    slug = re.sub(r'[^a-z0-9-]', '', slug)
    # Remove multiple consecutive hyphens
    slug = re.sub(r'-+', '-', slug)
    # Remove leading/trailing hyphens
    slug = slug.strip('-')
    return slug
'''

GENERATED_COMPOUND_INTEREST = '''
def compound_interest(principal: float, annual_rate: float, years: float, compounds_per_year: int) -> float:
    """Calculate compound interest for savings"""
    rate = annual_rate / 100
    amount = principal * ((1 + rate / compounds_per_year) ** (compounds_per_year * years))
    return round(amount, 2)
'''

GENERATED_TEXT_ANALYZER = '''
def analyze_text(text: str) -> dict:
    """Analyze text and return word count, character count, and average word length"""
    words = text.split()
    word_count = len(words)
    char_count = len(text)

    if word_count > 0:
        avg_word_length = char_count / word_count
    else:
        avg_word_length = 0

    return {
        'word_count': word_count,
        'char_count': char_count,
        'avg_word_length': round(avg_word_length, 2)
    }
'''

GENERATED_PRIME_CHECKER = '''
def is_prime(n: int) -> bool:
    """Check if a number is prime"""
    if n < 2:
        return False
    if n == 2:
        return True
    if n % 2 == 0:
        return False

    for i in range(3, int(n**0.5) + 1, 2):
        if n % i == 0:
            return False

    return True
'''


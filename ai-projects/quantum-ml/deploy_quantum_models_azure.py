# Trusted directory for loading model artifacts
TRUSTED_RESULTS_DIR = Path(__file__).parent / 'results'


def load_model_artifacts(model_name, n_qubits, n_layers):
    """Load trained model parameters from state dict.
    
    Security: Only loads from the trusted results directory within this package.
    Pickle files should only be generated by the training scripts in this repository.
    
    Args:
        model_name: Name of the trained model (e.g., 'heart_disease', 'ionosphere')
        n_qubits: Number of qubits used in the model
        n_layers: Number of layers in the quantum circuit
        
    Returns:
        Tuple of (params, scaler, pca) loaded from the results directory
        
    Raises:
        ValueError: If paths attempt to escape the trusted directory
        FileNotFoundError: If model artifacts don't exist
    """
    results_dir = TRUSTED_RESULTS_DIR
    
    # Validate model_name to prevent path traversal
    if not model_name or '..' in model_name or '/' in model_name or '\\' in model_name:
        raise ValueError(f"Invalid model name: {model_name}")
    
    # Load model state dict
    model_path = results_dir / f'{model_name}_model.pt'
    scaler_path = results_dir / f'{model_name}_scaler.pkl'
    pca_path = results_dir / f'{model_name}_pca.pkl'
    
    # Verify paths are within trusted directory
    for path in [model_path, scaler_path, pca_path]:
        resolved = path.resolve()
        if not str(resolved).startswith(str(results_dir.resolve())):
            raise ValueError(f"Path escapes trusted directory: {path}")
    
    print(f"📂 Loading {model_name} artifacts...")
    print(f"   Model: {model_path}")
    print(f"   Scaler: {scaler_path}")
    print(f"   PCA: {pca_path}")
    
    # Load trained weights
    state_dict = torch.load(model_path, weights_only=True)
    
    # Extract quantum layer parameters directly from state dict
    # The quantum layer params are stored under 'quantum_layer.qlayer.weights'
    params = state_dict['quantum_layer.qlayer.weights'].numpy()
    
    # Load sklearn preprocessors from trusted local files
    # Note: pickle.load is used here for sklearn compatibility.
    # These files should only be generated by training scripts in this repo.
    with open(scaler_path, 'rb') as f:
        scaler = pickle.load(f)
    
    with open(pca_path, 'rb') as f:
        pca = pickle.load(f)
    
    return params, scaler, pca

def create_test_circuit(n_qubits, n_layers, params):
    """Create a quantum circuit with trained parameters"""
    qc = QuantumCircuit(n_qubits, n_qubits)
    
    # Input layer - create superposition
    for qubit in range(n_qubits):
        qc.h(qubit)
    
    # Variational layers with trained parameters
    param_idx = 0
    for layer in range(n_layers):
        # Rotation layer
        for qubit in range(n_qubits):
            qc.ry(params[param_idx], qubit)
            param_idx += 1
            qc.rz(params[param_idx], qubit)
            param_idx += 1
        
        # Entanglement layer (linear)
        for qubit in range(n_qubits - 1):
            qc.cx(qubit, qubit + 1)
    
    # Measurement
    qc.measure_all()
    
    return qc

